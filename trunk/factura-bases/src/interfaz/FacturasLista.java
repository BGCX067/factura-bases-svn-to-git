            try {
                ArrayList<String> clave = new ArrayList();

                clave.add(Tfacturas.getValueAt(Tfacturas.getSelectedRow(), 0).toString());
                clave.add(Tfacturas.getValueAt(Tfacturas.getSelectedRow(), 1).toString());
                clave.add(Tfacturas.getValueAt(Tfacturas.getSelectedRow(), 2).toString());
                MostrarFactura mf = new MostrarFactura(clave, menu,this);
                this.setVisible(false);
                menu.escritorio.add(mf);
                mf.show();
            } // TODO add your handling code here:
