    private void actualizar() {
        try {
            ArrayList<Factura> facturas = facturaBD.listar();
            String[] columnas = {"IdFactura", "Fecha", "Cliente"};
            String[][] datos = new String[facturas.size()][3];
            for (int i = 0; i < facturas.size(); i++) {

                Factura f = facturas.get(i);
                datos[i][0] = f.getIdFactura();
                datos[i][1] = f.getFecha();
                datos[i][2] = f.getCedula();

            }
            DefaultTableModel aux = new DefaultTableModel(datos, columnas);
            Tfacturas.setModel(aux);
            //Tfacturas.setEnabled(false);
            
            Tfacturas.updateUI();

        } catch (SQLException ex) {
            Logger.getLogger(FacturasLista.class.getName()).log(Level.SEVERE, null, ex);
        }


    }
